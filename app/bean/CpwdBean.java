package bean;

public class CpwdBean {

	public String cpwd;
	public String npwd;
	public String rnpwd;
	
	public String getCpwd() {
		return cpwd;
	}
	public void setCpwd(String cpwd) {
		this.cpwd = cpwd;
	}
	public String getNpwd() {
		return npwd;
	}
	public void setNpwd(String npwd) {
		this.npwd = npwd;
	}
	public String getRnpwd() {
		return rnpwd;
	}
	public void setRnpwd(String rnpwd) {
		this.rnpwd = rnpwd;
	}
	
	
	
}
